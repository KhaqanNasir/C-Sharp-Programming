
using System;

namespace C_Sharp_Programs
{
     class DataType
    {
        static void Main(string[] args)
        {
            Console.WriteLine("..........Data Types..........");

            int x = 39;                 // int is the data type and it can store whole numbers
            Console.WriteLine(x);

            double y = 3.39503;        // double is the data type and it can store value of points greater than 7
            Console.WriteLine(y);

            float z = 3.85f;           // float is the data type and it can store points values
            Console.WriteLine(z);

            char ch = '@';            // char is the data type and it can store value pf single character
            Console.WriteLine(ch);

            string s = "Khaqan Nasir";// string is the data type and it can write in double quotes, used for print a message
            Console.WriteLine(s);

            bool b = true;           // Bool is the data type used for true or false
            Console.WriteLine(b);

            decimal d = 3.593945030m;// Decimal is the data type used for long values
            Console.WriteLine(d);

            Console.Read();         // Console.Read is used for hold the screen
        }
    }
}
